-- Security Alerts and Webhooks Infrastructure
-- Migration 010: Real-time security alerting system with notification channels

-- Security Alert Rules Configuration
CREATE TABLE security_alert_rules (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    name VARCHAR(255) NOT NULL,
    description TEXT,
    category VARCHAR(50) NOT NULL CHECK (category IN (
        'AUTHENTICATION', 'AUTHORIZATION', 'BRUTE_FORCE', 
        'INJECTION', 'PRIVILEGE_ESCALATION', 'DATA_BREACH',
        'ANOMALY', 'PERFORMANCE', 'SYSTEM'
    )),
    severity VARCHAR(20) NOT NULL CHECK (severity IN ('LOW', 'MEDIUM', 'HIGH', 'CRITICAL')),
    
    -- Rule configuration as JSONB for flexibility
    conditions JSONB NOT NULL, -- Rule conditions and thresholds
    aggregation_window_minutes INTEGER DEFAULT 5,
    threshold_value NUMERIC NOT NULL,
    threshold_operator VARCHAR(10) NOT NULL CHECK (threshold_operator IN ('>', '>=', '<', '<=', '=', '!=')),
    
    -- Rule status and metadata
    is_active BOOLEAN DEFAULT true,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    created_by VARCHAR(255),
    last_modified_by VARCHAR(255),
    
    -- Rate limiting to prevent alert spam
    cooldown_minutes INTEGER DEFAULT 15,
    max_alerts_per_hour INTEGER DEFAULT 10,
    
    -- Additional configuration
    tags JSONB DEFAULT '[]',
    custom_fields JSONB DEFAULT '{}',
    
    CONSTRAINT unique_rule_name UNIQUE (name)
);

-- Security Alerts Generated by Rules
CREATE TABLE security_alerts (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    rule_id UUID REFERENCES security_alert_rules(id) ON DELETE CASCADE,
    
    -- Alert identification
    title VARCHAR(500) NOT NULL,
    description TEXT,
    severity VARCHAR(20) NOT NULL CHECK (severity IN ('LOW', 'MEDIUM', 'HIGH', 'CRITICAL')),
    category VARCHAR(50) NOT NULL,
    
    -- Alert status and lifecycle
    status VARCHAR(20) DEFAULT 'ACTIVE' CHECK (status IN (
        'ACTIVE', 'ACKNOWLEDGED', 'RESOLVED', 'SUPPRESSED', 'ESCALATED'
    )),
    
    -- Timing information
    triggered_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    acknowledged_at TIMESTAMP WITH TIME ZONE,
    resolved_at TIMESTAMP WITH TIME ZONE,
    
    -- Associated data
    event_data JSONB NOT NULL, -- Related security events and context
    affected_entities JSONB DEFAULT '{}', -- Users, IPs, sessions affected
    correlation_id VARCHAR(255), -- For grouping related alerts
    
    -- Assignment and resolution
    assigned_to VARCHAR(255),
    acknowledged_by VARCHAR(255),
    resolved_by VARCHAR(255),
    resolution_notes TEXT,
    
    -- Alert metrics
    trigger_count INTEGER DEFAULT 1,
    escalation_level INTEGER DEFAULT 0,
    
    -- Additional metadata
    source_ip INET,
    user_id VARCHAR(255),
    session_id VARCHAR(255),
    custom_fields JSONB DEFAULT '{}',
    
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Webhook Endpoints Configuration
CREATE TABLE webhook_endpoints (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    name VARCHAR(255) NOT NULL,
    url TEXT NOT NULL,
    
    -- Webhook configuration
    http_method VARCHAR(10) DEFAULT 'POST' CHECK (http_method IN ('POST', 'PUT', 'PATCH')),
    content_type VARCHAR(100) DEFAULT 'application/json',
    
    -- Authentication
    auth_type VARCHAR(20) DEFAULT 'HMAC' CHECK (auth_type IN ('NONE', 'BASIC', 'BEARER', 'HMAC', 'CUSTOM')),
    auth_config JSONB DEFAULT '{}', -- Stores auth credentials/config
    
    -- Headers and payload customization
    custom_headers JSONB DEFAULT '{}',
    payload_template JSONB, -- Template for webhook payload
    
    -- Retry configuration
    retry_attempts INTEGER DEFAULT 3,
    retry_backoff_seconds INTEGER DEFAULT 60,
    timeout_seconds INTEGER DEFAULT 30,
    
    -- Filtering
    alert_severity_filter VARCHAR(50)[], -- Only send for specific severities
    alert_category_filter VARCHAR(50)[], -- Only send for specific categories
    
    -- Status and health
    is_active BOOLEAN DEFAULT true,
    last_success_at TIMESTAMP WITH TIME ZONE,
    last_failure_at TIMESTAMP WITH TIME ZONE,
    failure_count INTEGER DEFAULT 0,
    health_status VARCHAR(20) DEFAULT 'HEALTHY' CHECK (health_status IN ('HEALTHY', 'DEGRADED', 'FAILED')),
    
    -- Metadata
    description TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    created_by VARCHAR(255),
    
    CONSTRAINT unique_webhook_name UNIQUE (name)
);

-- Notification Channels Configuration
CREATE TABLE notification_channels (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    name VARCHAR(255) NOT NULL,
    channel_type VARCHAR(50) NOT NULL CHECK (channel_type IN (
        'WEBHOOK', 'EMAIL', 'SLACK', 'DISCORD', 'PAGERDUTY', 'SMS', 'TEAMS'
    )),
    
    -- Channel configuration
    config JSONB NOT NULL, -- Channel-specific configuration
    
    -- Filtering and routing
    alert_severity_filter VARCHAR(50)[], -- Only notify for specific severities
    alert_category_filter VARCHAR(50)[], -- Only notify for specific categories
    time_restrictions JSONB, -- When to send notifications
    
    -- Rate limiting
    rate_limit_per_minute INTEGER DEFAULT 10,
    rate_limit_per_hour INTEGER DEFAULT 60,
    
    -- Status
    is_active BOOLEAN DEFAULT true,
    last_used_at TIMESTAMP WITH TIME ZONE,
    
    -- Metadata
    description TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    
    CONSTRAINT unique_channel_name UNIQUE (name)
);

-- Notification Delivery Tracking
CREATE TABLE notification_deliveries (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    alert_id UUID REFERENCES security_alerts(id) ON DELETE CASCADE,
    channel_id UUID REFERENCES notification_channels(id) ON DELETE SET NULL,
    webhook_id UUID REFERENCES webhook_endpoints(id) ON DELETE SET NULL,
    
    -- Delivery details
    delivery_method VARCHAR(50) NOT NULL,
    recipient VARCHAR(500),
    
    -- Status tracking
    status VARCHAR(20) DEFAULT 'PENDING' CHECK (status IN (
        'PENDING', 'SENT', 'DELIVERED', 'FAILED', 'RETRYING'
    )),
    
    -- Timing
    scheduled_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    sent_at TIMESTAMP WITH TIME ZONE,
    delivered_at TIMESTAMP WITH TIME ZONE,
    
    -- Error handling
    attempt_count INTEGER DEFAULT 0,
    max_attempts INTEGER DEFAULT 3,
    last_error TEXT,
    
    -- Delivery metadata
    request_id VARCHAR(255),
    response_code INTEGER,
    response_body TEXT,
    
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Alert Acknowledgment and Comments
CREATE TABLE alert_comments (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    alert_id UUID REFERENCES security_alerts(id) ON DELETE CASCADE,
    
    -- Comment details
    comment_text TEXT NOT NULL,
    comment_type VARCHAR(20) DEFAULT 'COMMENT' CHECK (comment_type IN (
        'COMMENT', 'ACKNOWLEDGMENT', 'RESOLUTION', 'ESCALATION', 'STATUS_CHANGE'
    )),
    
    -- Author information
    author VARCHAR(255) NOT NULL,
    author_role VARCHAR(100),
    
    -- Metadata
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    is_internal BOOLEAN DEFAULT false -- Internal vs public comments
);

-- Alert Escalation Rules
CREATE TABLE alert_escalation_rules (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    name VARCHAR(255) NOT NULL,
    
    -- Escalation conditions
    severity VARCHAR(20) NOT NULL,
    category VARCHAR(50),
    unacknowledged_minutes INTEGER NOT NULL,
    
    -- Escalation actions
    escalate_to_severity VARCHAR(20) CHECK (escalate_to_severity IN ('MEDIUM', 'HIGH', 'CRITICAL')),
    notify_channels UUID[], -- Array of notification channel IDs
    assign_to VARCHAR(255),
    
    -- Rule configuration
    is_active BOOLEAN DEFAULT true,
    priority INTEGER DEFAULT 100,
    
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Alert Suppression Rules
CREATE TABLE alert_suppressions (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    name VARCHAR(255) NOT NULL,
    
    -- Suppression criteria
    alert_rule_id UUID REFERENCES security_alert_rules(id) ON DELETE CASCADE,
    conditions JSONB NOT NULL, -- Conditions for suppression
    
    -- Suppression window
    start_time TIMESTAMP WITH TIME ZONE,
    end_time TIMESTAMP WITH TIME ZONE,
    
    -- Recurring suppression
    is_recurring BOOLEAN DEFAULT false,
    recurrence_pattern JSONB, -- For recurring suppressions
    
    -- Status
    is_active BOOLEAN DEFAULT true,
    
    -- Metadata
    reason TEXT,
    created_by VARCHAR(255),
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Alert Correlation and Grouping
CREATE TABLE alert_correlations (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    correlation_id VARCHAR(255) NOT NULL,
    
    -- Correlation details
    alert_ids UUID[] NOT NULL,
    correlation_type VARCHAR(50) NOT NULL CHECK (correlation_type IN (
        'TIME_BASED', 'IP_BASED', 'USER_BASED', 'SESSION_BASED', 'PATTERN_BASED'
    )),
    
    -- Correlation metadata
    root_cause_alert_id UUID,
    correlation_score NUMERIC(5,4),
    
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    
    CONSTRAINT unique_correlation UNIQUE (correlation_id)
);

